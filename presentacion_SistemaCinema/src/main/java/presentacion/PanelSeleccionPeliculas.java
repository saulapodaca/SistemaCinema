        lblImagen.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent e) {
                ControlPantallas.getInstance().abrirSeleccionFunciones(PanelSeleccionPeliculas.this,peli);
            }
        });
